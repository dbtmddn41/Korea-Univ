def jamo2engkey(jamo_str):
    engkey = ''
    for letter in jamo_str:
        if letter in _JAMO2ENGKEY_:
            engkey += _JAMO2ENGKEY_[letter]
        else:
            engkey += letter
    return engkey


def engkey2jamo(key_str):
    han_lett = ''
    for letter in key_str:
        if letter in _ENGKEY2JAMO_:
            han_lett += _ENGKEY2JAMO_[letter]
        else:
            han_lett += letter
    return han_lett

def is_cho(l):
    return l in _CHO_

def is_jung(l):
    return l in _JUNG_

def is_jong(l):
    return l in _JONG_

def find_index(cho, jung, jong):
    for i in range(19):
        if cho == _CHO_[i]:
            cho = i
            break
    for i in range(21):
        if jung == _JUNG_[i]:
            jung = i
            break
    
    if jong == '':
        jong = 0
    else:
        for i in range(27):
            if jong == _JONG_[i]:
                jong = i+1
                break
    return cho, jung, jong

def count_jung(s):
    if is_jung(s[0]):
        if len(s) > 1:
            if is_jung(s[1]):
                if s[:2] in _DOUBLE_MOEM_:
                    return 2, _DOUBLE_MOEM_[s[:2]]
        return 1, s[0]
    else:
        return 0, None


def count_jong(s):
    num = 0
    len_s = len(s)
    for i in range(2):
        if len_s <= i:
            break
        if is_jong(s[i]):
            num += 1
        else:
            break
        if len_s <= i+1:
            break
        if is_jung(s[i+1]):
            num -= 1
            break
    if num == 2:
        if s[:2] in _DOUBLE_JAEM_:
            return 2, _DOUBLE_JAEM_[s[:2]]
        else:
         return 1, s[0]
    return num, s[:num]

def jamo2syllable(s):
    syllable = ''
    len_str = len(s)
    i=0

    while i < len_str:
        jung_flag = 0
        
        cho_num, cho = count_jong(s[i:])
        if cho_num == 2:
            syllable += cho
            i += 2
            continue
        else:
            cho = s[i]
            if not is_cho(s[i]):
                if not is_jung(s[i]):
                    syllable += s[i]
                    i += 1
                    continue
                else:
                    jung_flag = 1
            else:
                i += 1

        if i >= len_str:
            syllable += cho
            break
        
        jung_num, jung = count_jung(s[i:])
        if jung_num == 0:
            syllable += cho
            continue
        i += jung_num
        if jung_flag:
            syllable += jung
            continue

        jong = ''
        if i < len_str:
            jong_num, jong = count_jong(s[i:])
            if jong_num != 0:
                i += jong_num

        cho, jung, jong = find_index(cho, jung, jong)
        syllable += compose(cho, jung, jong)
        
    return syllable

